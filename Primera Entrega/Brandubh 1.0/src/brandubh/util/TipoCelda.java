package brandubh.util;

/**
 * Esta enumeración representa los tipos de celdas en un tablero de juego. Los
 * tipos posibles son TRONO (celda del trono), PROVINCIA (celda de provincia) y
 * NORMAL (celda normal).
 * 
 * @author Jon Ander Incera Moreno 
 * Creado 31/10/2023
 * @version 1.0 realizacion del codigo 1.1 comentarios
 * 
 */
public enum TipoCelda {
	/** Representa una celda que es el trono del rey. */
	TRONO,
	/** Representa una celda que es una provincia. */
	PROVINCIA,
	/** Representa una celda normal sin características especiales. */
	NORMAL;
}
